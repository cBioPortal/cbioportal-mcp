-- ============================================================================
-- cancer_study_query_preferences: which study (or set of studies) to use
-- for which kind of question
-- ============================================================================
-- One row = "for purpose X, use cancer_study_identifier Y". A preference
-- can name a single study (e.g. treatment_outcomes → msk_chord_2024) or a
-- whole cohort (e.g. all_studies_non_redundant → the 242 curated studies). The LLM
-- joins on this table instead of hardcoding study IDs.
--
-- Preferences shipped here:
--   pan_cancer_tcga          32 studies — the TCGA PanCancer Atlas 2018 set
--                            (auto-detected via the _tcga_pan_can_atlas_2018
--                            suffix). One sample per patient, no overlap.
--                            Use for TCGA-only cross-cancer-type questions.
--   all_studies_non_redundant        242 studies — cBioPortal's manually-curated
--                            "non-redundant" set (--skin.quick_select_buttons).
--                            No overlapping samples across studies; widest
--                            pan-cancer coverage. Default for "across cancer
--                            types" questions.
--   large_genomic_cohort      1 study  — msk_impact_50k_2026. Biggest single
--                            MSK-IMPACT cohort (~50k samples). Use when the
--                            question is about genomic patterns (mutation
--                            frequency, co-occurrence) at maximum power.
--   treatment_outcomes        1 study  — msk_chord_2024. Rich treatment and
--                            outcomes data in clinical_event_derived. Use
--                            for treatment-related questions.
--
-- Refreshed on every clone (DROP + CREATE). Studies absent from the current
-- clone just won't join to any downstream rows.
-- ============================================================================

DROP TABLE IF EXISTS cancer_study_query_preferences;

CREATE TABLE cancer_study_query_preferences (
    preference_name LowCardinality(String) COMMENT 'Named purpose: all_studies_non_redundant | pan_cancer_tcga | large_genomic_cohort | treatment_outcomes',
    cancer_study_identifier String         COMMENT 'cancer_study.cancer_study_identifier — join key',
    notes String                           COMMENT 'Why this study is recommended for this preference'
)
ENGINE = MergeTree
ORDER BY (preference_name, cancer_study_identifier)
COMMENT 'Curated study recommendations per query intent. Lookup by preference_name, then JOIN on cancer_study_identifier.';

-- pan_cancer_tcga: auto-populated from whatever TCGA PanCanAtlas studies are loaded
INSERT INTO cancer_study_query_preferences
SELECT 'pan_cancer_tcga', cancer_study_identifier, 'TCGA PanCancer Atlas 2018'
FROM cancer_study
WHERE cancer_study_identifier LIKE '%_tcga_pan_can_atlas_2018';

